package RestassuredTraining;

public class POSTRequestCreationUsingPOJOClass {

    String name;
    int age;
    String grade;
    String subjectsArr[];

    public String[] getSubjectsArr() {
        return subjectsArr;
    }

    public void setSubjectsArr(String[] subjectsArr) {
        this.subjectsArr = subjectsArr;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getGrade() {
        return grade;
    }

    public void setGrade(String grade) {
        this.grade = grade;
    }








}
